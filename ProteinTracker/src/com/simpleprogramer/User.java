package com.simpleprogramer;

public class User {
private int id;
private int total;
private int goal;
private String name;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getTotal() {
	return total;
}
public void setTotal(int total) {
	this.total = total;
}
public int getGoal() {
	return goal;
}
public void setGoal(int goal) {
	this.goal = goal;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}




}
